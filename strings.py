# Strings

name = 'Brad'
age = 37

# concatenate
print('Hello, my name is ' + name + ' and I am ' + str(age))

# String formatting

# Arguments by position
print('My name is {name} and I am {age}'.format(name=name, age=age) )

# F-Strings (3.6+)
print(f'Hello, my name is {name} and I am {age}')

